# List of animals and statements about them

animals = ["cat", "rabbit", "koala"]

# Print the name of each animal
for animal in animals:
    print(animal)

print("\nStatements about each animal:")

# Print a statement for each animal
for animal in animals:
    print(f"A {animal} would make a great pet.")

# Print a statement about what these animals have in common
print("\nAll of these animals are cute and gentle companions!")
